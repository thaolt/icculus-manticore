<div class="headline">Manticore goes public</div>
<div class="date">[posted 29/04/02 by Benj]</div>

<p class="newsitem">Now that we've made it out of our fourth-year
design project lab, Manticore is ready to go public.  CVS has been
kindly set up by
<a href="http://www.icculus.org">icculus.org</a> as has this
webspace.</p>

<p class="newsitem">Formerly called &quot;The HULK&quot;, Manticore is
an open source hardware design for a 3D graphics accelerator.  It is
written entirely in VHDL.  Originally designed and tested using
<a href="http://www.altera.com">Altera's</a> APEX20K200E FPGA and
Nios development board, the design is now ready for the public.</p>

<p class="newsitem">The project was started by <a
href="mailto:jm@icculus.org">Jeff Mrochuk</a> and myself (<a
href="mailto:benjcarson@digitaljunkies.ca">Benj Carson</a>) and is currently
maintained by us.  If you are interested in contributing to the
project, please send us an email or join the <a
href="mailto:manticore-subscribe@icculus.org">mailing list</a>.</p>

