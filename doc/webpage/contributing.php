<h2><a name="cvs">CVS</a></h2>
<p>Manticore's source can be checked out of CVS using the &quot;pserver&quot; method.
To access CVS anonymously: (type this all on one line)</p>

<div class="command">cvs -z3 -d:pserver:anonymous@cvs.icculus.org:/cvs/cvsroot login</div>
<p>(password is &quot;anonymous&quot; without the quotes)</p>
<div class="command">cvs -z3 -d:pserver:anonymous@cvs.icculus.org:/cvs/cvsroot co manticore</div>

<p>Web CVS is also provided
<a href="http://cvs.icculus.org/horde/chora/cvs.php?rt=manticore">here</a>.</p>

